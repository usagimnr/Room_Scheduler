    public void rebulidFacultyDisplay(){
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByFaculty((facultyStatusComboBox.getSelectedItem().toString()));
        String display = "";
        String statusLabel = "";
        for(ReservationEntry reservation: reservations)
        {
            display += String.format("[Reserved for] Faculty: %s, Room: %s, Seats: %d, Date: %s %n", reservation.getFaculty(), reservation.getRoom(), reservation.getSeats(), reservation.getDate());
        }
        
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getWaitlistByFaculty((facultyStatusComboBox.getSelectedItem().toString()));
        for(WaitlistEntry waitListEntry: waitlist){
            display += String.format("[Waitlisted for]Faculty: %s, Seats: %d, Date: %s, Timestamp: %s %n", waitListEntry.getFaculty(), waitListEntry.getSeats(), waitListEntry.getDate(), waitListEntry.getTimestamp());
        }
        facultyDisplay.setText(display);
    }

