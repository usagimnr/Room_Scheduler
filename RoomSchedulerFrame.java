    private void cancelSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelSubmitButtonActionPerformed
        // TODO add your handling code here:
        String faculty = cancelFacultyComboBox.getSelectedItem().toString();
        Date date = Date.valueOf(cancelDateComboBox.getSelectedItem().toString());
        String roomName = "";
        ArrayList<ReservationEntry> facultyRoom = ReservationQueries.getReservationsByFaculty(faculty);
        for(ReservationEntry reservation: facultyRoom){
            if(reservation.getDate().equals(date)){
                roomName = reservation.getRoom();
            }
            else{
                roomName = "";
            }
        }
        ReservationQueries.cancelReservation(faculty, date);
        String titleText = "";
        titleText += ("The reservation for " + faculty + " on " + date + " has been cancelled." + "\n");
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByDate();
        for(ReservationEntry reservation: reservations){
            if(reservation.getRoom().equals(roomName)) {
                titleText += (reservation.getFaculty() + " has reserved this room " + reservation.getRoom() + "." + "\n");
            }
        }
        cancelReservationDisplay.setText(titleText);
    }//GEN-LAST:event_cancelSubmitButtonActionPerformed

