    private void addRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRoomButtonActionPerformed
        // TODO add your handling code here:
        String name = addRoomTextField.getText();
        String seat = seatsTextField.getText();
        int seats = Integer.parseInt(seat);
        RoomQueries.addRoom(name, seats);
        String titleText = "";
        titleText += ("The room " + name + " with " + seats + " seats has been added." + "\n"); 
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByDate();
        for(ReservationEntry reservation: reservations){
            if(reservation.getRoom().equals(name)) {
                titleText += (reservation.getFaculty() + " has reserved this room for " + reservation.getDate() + "." + "\n");
            }
        }
        addRoomDisplay.setText(titleText);
        rebuildRoomComboBoxes();
    }//GEN-LAST:event_addRoomButtonActionPerformed

