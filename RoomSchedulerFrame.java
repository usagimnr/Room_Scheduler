    private void dropRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropRoomButtonActionPerformed
        // TODO add your handling code here:
        String room = roomComboBox.getSelectedItem().toString();
        String titleText = "";
        String facultyName = "";
        String dateName = "";
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByDate();
        for(ReservationEntry reservation: reservations){
            if(reservation.getRoom().equals(room)) {
                titleText += ("Faculty " + reservation.getFaculty() + " has been moved to the waitlist for date " + reservation.getDate() + "." + "\n");
                facultyName += reservation.getFaculty();
                dateName += reservation.getDate().toString();
            }
        }
        RoomQueries.dropRoom(room);
        dropRoomDisplay.setText(titleText);
    }//GEN-LAST:event_dropRoomButtonActionPerformed

    private void displayReservationStatusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayReservationStatusButtonActionPerformed
        // TODO add your handling code here:
        String titleText = "";
        titleText += ("Faculty Name:" + "\t" + "Room Name:" + "\t" + "Seats:" + "\t" + "Date:" + "\n"); 
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByDate(Date.valueOf(dateReservationComboBox.getSelectedItem().toString()));
        for(ReservationEntry reservation: reservations)
        {
            titleText += String.format("%s" + "\t" + "%d" + "\t" + "%s" + "\t" + "%s%n", reservation.getFaculty(), reservation.getSeats(), reservation.getRoom(), reservation.getDate());
        }
        reservationListDisplay.setText(titleText);
        
    }//GEN-LAST:event_displayReservationStatusButtonActionPerformed

    private void refreshWaitlistButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshWaitlistButtonActionPerformed
        // TODO add your handling code here:
        String titleText = "";
        titleText += ("Faculty Name:" + "\t" + "Seats:" + "\t" + "Date:" + "\t" + "Timestamp: \n"); 
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getWaitlistByDate();
        for(WaitlistEntry waitListEntry: waitlist){
            titleText += String.format("%s" + "\t" + "%d" + "\t" + "%s" + "\t" + "%s%n", waitListEntry.getFaculty(), waitListEntry.getSeats(), waitListEntry.getDate(), waitListEntry.getTimestamp());
        }
        waitlistDisplay.setText(titleText);
    }//GEN-LAST:event_refreshWaitlistButtonActionPerformed

    private void displayFacultyStatusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayFacultyStatusButtonActionPerformed
        // TODO add your handling code here:
        String titleText = "";
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByFaculty(facultyStatusComboBox.getSelectedItem().toString());
        titleText += ("[Reserved for] \n Room Name:" + "\t" + "Date:" + "\n"); 
        for(ReservationEntry reservation: reservations)
        {
            titleText += String.format("%s" + "\t" + "%s%n", reservation.getRoom(), reservation.getDate());
        }
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getWaitlistByFaculty(facultyStatusComboBox.getSelectedItem().toString());
        titleText += ("\n[Waitlisted for] \n" + "Seats:" + "\t" + "Date:" + "\n");
        for(WaitlistEntry waitListEntry: waitlist){
            titleText += String.format("%d" + "\t" + "%s%n", waitListEntry.getSeats(), waitListEntry.getDate());
        }
        facultyDisplay.setText(titleText);
    }//GEN-LAST:event_displayFacultyStatusButtonActionPerformed

